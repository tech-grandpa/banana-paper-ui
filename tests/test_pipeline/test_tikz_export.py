@pytest.mark.asyncio
async def test_pipeline_export_tikz_failure_is_surfaced(tmp_path, monkeypatch):
    """A TikZ export failure does not break the run and is reported via the
    TIKZ_EXPORTER_END progress event with an error payload."""
    events: list[PipelineProgressEvent] = []

    settings = Settings(
        output_dir=str(tmp_path / "outputs"),
        reference_set_path=str(tmp_path / "empty_refs"),
        refinement_iterations=1,
        save_iterations=False,
        export_tikz=True,
    )
    vlm = _MockVLM(
        responses=[
            "Initial plan description",
            "Styled final description",
            json.dumps({"critic_suggestions": [], "revised_description": None}),
        ]
    )
    image_gen = _MockImageGen()
    pipeline = PaperBananaPipeline(settings=settings, vlm_client=vlm, image_gen_fn=image_gen)

    async def _boom(*args, **kwargs):
        raise RuntimeError("VLM exploded")

    monkeypatch.setattr(pipeline.tikz_exporter, "run", _boom)

    result = await pipeline.generate(
        GenerationInput(
            source_context="Minimal context",
            communicative_intent="Minimal caption",
            diagram_type=DiagramType.METHODOLOGY,
        ),
        progress_callback=events.append,
    )

    # Image generation still succeeds; no .tex is produced.
    assert Path(result.image_path).exists()
    assert result.tikz_path is None

    end_events = [e for e in events if e.stage == PipelineProgressStage.TIKZ_EXPORTER_END]
    assert len(end_events) == 1
    assert end_events[0].extra is not None
    assert "VLM exploded" in (end_events[0].extra.get("error") or "")
