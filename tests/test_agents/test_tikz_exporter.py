# ── Agent run ──────────────────────────────────────────────────────


@pytest.mark.asyncio
async def test_run_returns_tikz_with_header(tmp_path):
    """run() returns TikZ code preceded by a metadata header."""
    # Create a minimal test image
    from PIL import Image

    img = Image.new("RGB", (100, 100), color="white")
    img_path = tmp_path / "test.png"
    img.save(str(img_path))

    fenced_response = f"```tikz\n{SAMPLE_TIKZ}\n```"
    vlm = MockVLM(response=fenced_response)
    agent = TikZExporterAgent(vlm)

    result = await agent.run(
        image_path=str(img_path),
        source_context="test methodology",
        caption="test caption",
        diagram_type=DiagramType.METHODOLOGY,
        description="test description",
        venue="neurips",
        version="0.1.0",
    )

    assert SAMPLE_TIKZ in result
    assert "% Generated by PaperBanana 0.1.0" in result
    assert "% Venue: neurips" in result
    assert "% Diagram type: methodology" in result


